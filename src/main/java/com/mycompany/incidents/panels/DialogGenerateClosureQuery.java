  private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
		if(comboQueryType.getSelectedIndex()==-1){
			outputTxt.setText("No se ha seleccionado el tipo de query");
		  outputTxt.setCaretPosition(0);
			return;
		}
		configureDate();
		ClosureQueryTypeEnum queryType = ClosureQueryTypeEnum.valueOf(
						                         comboQueryType.getSelectedItem().toString());
		switch (queryType) {
			case CierreFinancieroGastos:
				generateCierreFinancieroGastos();
				break;
			case CierreFinancieroPagos:
				generateCierreFinancieroPagos();
				break;
			case CierreFinancieroReservas:
				generateCierreFinancieroReservas();
				break;
			case CierreFinancieroSalvamentos:
				generateCierreFinancieroSalvamentos();
				break;
			case CierreReaseguroCuentas:
				generateCierreReaseguroCuentas();
				break;
			case CierreReaseguroGW:
				generateCierreReaseguroGW();
				break;
			case CierreReaseguroValidaciones:
				generateCierreReaseguroValidaciones();
				break;
		}
  }//GEN-LAST:event_jButton1ActionPerformed

