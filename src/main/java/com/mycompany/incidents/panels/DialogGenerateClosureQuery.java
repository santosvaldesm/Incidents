	private void configureDate() {
		try {
			SimpleDateFormat sdf    = new SimpleDateFormat("dd-MM-YYYY");
			String parseInitialDate = sdf.format(initialDateChooser.getDate());
			String parseEndDate     = sdf.format(endDateChooser.getDate());			
			String[] splitInitialDate = parseInitialDate.split("-");
			String[] splitEndDate = parseEndDate.split("-");
			intialDay   = splitInitialDate[0];
			intialYear  = splitInitialDate[2];
			intialMonth = splitInitialDate[1];
			endDay      = splitEndDate[0];			
			endMonth    = splitEndDate[1];
			endYear     = splitEndDate[2];

		} catch (Exception e) {
			outputTxt.setText("El formato de las fechas no es correcto");
		  outputTxt.setCaretPosition(0);
			//IncidentsUtil.printStackTrace(e,outputTxt);
		}
	}

